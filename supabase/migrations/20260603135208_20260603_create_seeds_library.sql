/*
  # Seeds Library — بذور جاهزة للزراعة

  ## New Tables
  - `seeds`: Ready-made business ideas library
    - Slug-based routing, sector classification, full Arabic content fields
    - Financial metrics: investment_min/max, roi_estimate, break_even_months
    - Capacity tracking: max_spots, spots_taken
    - Status lifecycle: active | draft | archived

  - `seed_grabs`: Tracks which founders grabbed which seeds
    - seed_id + user_id unique constraint prevents duplicate grabs
    - idea_id links to the created idea in the founder's dashboard

  ## Functions
  - `grab_seed(p_seed_id, p_user_id)`: Atomic grab — locks seed row,
    validates availability, creates an idea, records the grab, increments counters.
    Uses SECURITY DEFINER to bypass RLS safely.

  ## Security
  - seeds: public SELECT for active seeds (no auth required for browsing)
  - seed_grabs: authenticated users read/insert their own rows only
  - No public UPDATE/DELETE on seeds (counters managed via grab_seed function)

  ## Seed Data
  - 5 Food & Agriculture ideas (sector_id: food)
*/

-- ─── Seeds table ─────────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS seeds (
  id                 uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  slug               text        UNIQUE NOT NULL,
  name               text        NOT NULL,
  sector_id          text        NOT NULL,
  sector_emoji       text        NOT NULL DEFAULT '🌱',
  sector_label       text        NOT NULL,
  short_description  text,
  problem            text,
  solution           text,
  target_customer    text,
  revenue_model      text,
  financial_estimates text,
  why_it_works       text,
  risks              text,
  best_markets       text,
  quick_start_steps  text,
  investment_min     integer     DEFAULT 0,
  investment_max     integer     DEFAULT 0,
  roi_estimate       integer     DEFAULT 0,
  break_even_months  integer     DEFAULT 0,
  max_spots          integer     DEFAULT 3,
  spots_taken        integer     DEFAULT 0,
  status             text        DEFAULT 'active' CHECK (status IN ('active','draft','archived')),
  views              integer     DEFAULT 0,
  grabs              integer     DEFAULT 0,
  created_at         timestamptz DEFAULT now(),
  updated_at         timestamptz DEFAULT now()
);

CREATE INDEX IF NOT EXISTS seeds_sector_id_idx ON seeds(sector_id);
CREATE INDEX IF NOT EXISTS seeds_status_idx    ON seeds(status);

-- ─── Seed grabs table ────────────────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS seed_grabs (
  id         uuid        PRIMARY KEY DEFAULT gen_random_uuid(),
  seed_id    uuid        NOT NULL REFERENCES seeds(id) ON DELETE CASCADE,
  user_id    uuid        NOT NULL,
  idea_id    uuid        REFERENCES ideas(id) ON DELETE SET NULL,
  created_at timestamptz DEFAULT now(),
  UNIQUE(seed_id, user_id)
);

CREATE INDEX IF NOT EXISTS seed_grabs_user_id_idx ON seed_grabs(user_id);
CREATE INDEX IF NOT EXISTS seed_grabs_seed_id_idx ON seed_grabs(seed_id);

-- ─── RLS ─────────────────────────────────────────────────────────────────────
ALTER TABLE seeds      ENABLE ROW LEVEL SECURITY;
ALTER TABLE seed_grabs ENABLE ROW LEVEL SECURITY;

-- Public can browse active seeds
CREATE POLICY "Anyone can read active seeds"
  ON seeds FOR SELECT
  USING (status = 'active');

-- Authenticated users manage their own grabs
CREATE POLICY "Users can read own grabs"
  ON seed_grabs FOR SELECT
  TO authenticated
  USING (auth.uid() = user_id);

CREATE POLICY "Users can insert own grabs"
  ON seed_grabs FOR INSERT
  TO authenticated
  WITH CHECK (auth.uid() = user_id);

-- ─── Atomic grab function ────────────────────────────────────────────────────
CREATE OR REPLACE FUNCTION grab_seed(p_seed_id uuid, p_user_id uuid)
RETURNS uuid
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_idea_id uuid;
  v_seed    record;
BEGIN
  -- Lock the seed row to prevent race conditions
  SELECT * INTO v_seed
  FROM seeds
  WHERE id = p_seed_id AND status = 'active'
  FOR UPDATE;

  IF v_seed IS NULL THEN
    RAISE EXCEPTION 'SEED_NOT_FOUND';
  END IF;

  IF v_seed.spots_taken >= v_seed.max_spots THEN
    RAISE EXCEPTION 'NO_SPOTS';
  END IF;

  IF EXISTS (
    SELECT 1 FROM seed_grabs
    WHERE seed_id = p_seed_id AND user_id = p_user_id
  ) THEN
    RAISE EXCEPTION 'ALREADY_GRABBED';
  END IF;

  -- Create idea in founder's dashboard
  INSERT INTO ideas (user_id, title, description, sector, stage, week, validation_score)
  VALUES (p_user_id, v_seed.name, v_seed.short_description, v_seed.sector_id, 'ideation', 1, 0)
  RETURNING id INTO v_idea_id;

  -- Record the grab
  INSERT INTO seed_grabs (seed_id, user_id, idea_id)
  VALUES (p_seed_id, p_user_id, v_idea_id);

  -- Increment counters
  UPDATE seeds
  SET spots_taken = spots_taken + 1,
      grabs       = grabs + 1,
      updated_at  = now()
  WHERE id = p_seed_id;

  RETURN v_idea_id;
END;
$$;

GRANT EXECUTE ON FUNCTION grab_seed(uuid, uuid) TO authenticated;

